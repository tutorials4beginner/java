
public class Employee {
	int empid;
	String name;
	String designation;
	String address;
	String gender;
	String dob;
	String dateOfJoining;

	public Employee(int empid, String name, String designation, String address, String gender, String dob,
			String dateOfJoining) {
		super();
		this.empid = empid;
		this.name = name;
		this.designation = designation;
		this.address = address;
		this.gender = gender;
		this.dob = dob;
		this.dateOfJoining = dateOfJoining;
	}
}
